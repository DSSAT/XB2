    private void Update() {
        if(bDay)
        {
            try
            {
                chemApp.CDAY = Integer.parseInt(txtCDAY.getText());
            }
            catch(Exception ex)
            {
                chemApp.CDAY = null;
            }
            chemApp.CDATE = null;
        }
        else
        {
            try
            {
                chemApp.CDATE = dpCDATE.getDate();
            }
            catch(Exception ex)
            {
                chemApp.CDATE = null;
            }
            chemApp.CDAY = null;
        }

        try
        {
            chemApp.CHAMT = Float.parseFloat(txtCHAMT.getText());
        }
        catch(Exception ex)
        {
            chemApp.CHAMT = null;
        }

        try
        {
            chemApp.CHDEP = Integer.parseInt(txtCHDEP.getText());
        }
        catch(Exception ex) {
            chemApp.CHDEP = null;
        }
        chemApp.CHT = txtCHT.getText();
    }

    private void LoadChemApp() {
        if(bDay)
        {
            try
            {
                txtCDAY.setText(chemApp.CDAY.toString());
            }
            catch(Exception ex)
            {
                txtCDAY.setText("");
            }
            dpCDATE.setDate(null);
        }
        else
        {
            try
            {
                dpCDATE.setDate(chemApp.CDATE);
            }
            catch(Exception ex)
            {
                dpCDATE.setDate(null);
            }
            txtCDAY.setText("");
        }

        try
        {
            txtCHAMT.setText(chemApp.CHAMT.toString());
        }
        catch(Exception ex) { }
        
        try
        {
            txtCHDEP.setText(chemApp.CHDEP.toString());
        }
        catch(Exception ex) { }
        try
        {
            txtCHT.setText(chemApp.CHT);
        }
        catch(Exception ex) { }
    }

