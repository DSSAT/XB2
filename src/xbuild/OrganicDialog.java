    private void Update(){
        if(bDay)
        {
            try
            {
                organicApp.RDAY = Integer.parseInt(txtRDATE.getText());
            }
            catch(Exception ex)
            {
                organicApp.RDAY = null;
            }
        }
        else
        {
            try
            {
                organicApp.RDATE = dpRDATE.getDate();
            }
            catch(Exception ex)
            {
                organicApp.RDATE = null;
            }
        }

        try
        {
            organicApp.RAMT = Integer.parseInt(txtRAMT.getText());
        }
        catch(Exception ex)
        {
            organicApp.RAMT = null;
        }

        try
        {
            organicApp.RESN = Float.parseFloat(txtRESN.getText());
        }
        catch(Exception ex)
        {
            organicApp.RESN = null;
        }

        try
        {
            organicApp.RESP = Float.parseFloat(txtRESP.getText());
        }
        catch(Exception ex)
        {
            organicApp.RESP = null;
        }

        try
        {
            organicApp.RESK = Float.parseFloat(txtRESK.getText());
        }
        catch(Exception ex)
        {
            organicApp.RESK = null;
        }

        try
        {
            organicApp.RINP = Integer.parseInt(txtRINP.getText());
        }
        catch(Exception ex)
        {
            organicApp.RINP = null;
        }

        try
        {
            organicApp.RDEP = Integer.parseInt(txtRDEP.getText());
        }
        catch(Exception ex)
        {
            organicApp.RDEP = null;
        }
    }

    private void LoadOrganicApp() {
        if(bDay)
        {
            try
            {
                txtRDATE.setText(organicApp.RDAY.toString());
            }
            catch(Exception ex)
            {
                txtRDATE.setText("");
            }
        }
        else
        {
            try
            {
                dpRDATE.setDate(organicApp.RDATE);
            }
            catch(Exception ex)
            {
                dpRDATE.setDate(null);
            }
        }

        try
        {
            txtRAMT.setText(organicApp.RAMT.toString());
        }
        catch(Exception ex)
        {
            txtRAMT.setText("");
        }

        try
        {
            txtRESN.setText(organicApp.RESN.toString());
        }
        catch(Exception ex)
        {
            txtRESN.setText("");
        }

        try
        {
            txtRESP.setText(organicApp.RESP.toString());
        }
        catch(Exception ex)
        {
            txtRESP.setText("");
        }

        try
        {
            txtRESK.setText(organicApp.RESK.toString());
        }
        catch(Exception ex)
        {
            txtRESK.setText("");
        }

        try
        {
            txtRINP.setText(organicApp.RINP.toString());
        }
        catch(Exception ex)
        {
            txtRINP.setText("");
        }

        try
        {
            txtRDEP.setText(organicApp.RDEP.toString());
        }
        catch(Exception ex)
        {
            txtRDEP.setText("");
        }
    }

